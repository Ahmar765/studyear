/** Dedupe + alphabetical sort for stable dropdowns */
function uniqSorted(arr: readonly string[]): string[] {
    return [...new Set(arr.map((s) => s.trim()).filter(Boolean))].sort((a, b) =>
        a.localeCompare(b, undefined, { sensitivity: 'base' }),
    );
}

