/**
 * Subjects available when a study level is chosen. Each level offers the full catalogue
 * so learners can match school offerings; university level also includes named degree routes.
 */
export const subjectsByLevel: Record<string, string[]> = Object.fromEntries(
    levels.map((level) => {
        if (level === 'University / Degree') {
            return [level, uniqSorted([...universityCourses, ...subjects])];
        }
        return [level, subjects];
    }),
) as Record<string, string[]>;


